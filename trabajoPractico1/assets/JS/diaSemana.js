let diaSemana = parseInt(prompt("ingrese un Número del 1 al 7:"));

switch (diaSemana) {
    case 1: diaSemana==1;
        console.log("Es Lunes.")
        
        break;
    case 2: diaSemana==2;
        console.log("Es Martes.")
        
    break;

    case 3: diaSemana==3;
        console.log("Es Miercoles.")
            
    break;

    case 4: diaSemana==4;
        console.log("Es Jueves.")
            
    break;

    case 5: diaSemana==5;
        console.log("Es Viernes.")
            
    break;

    case 6: diaSemana==6;
        console.log("Es Sabado.")
            
    break;
    case 7: diaSemana==7;
        console.log("Es Domingo.")
            
    break;

    default:
        console.log("Ingrese un día Valido.")
    break;
}